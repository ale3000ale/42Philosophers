#include "philo.h"

static void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = s;
	while (i < n)
	{
		str[i] = '\0';
		i++;
	}
}

void	*ft_calloc(size_t count, size_t size)
{
	void		*array;

	array = (void *)malloc(count * size);
	if (!array)
		return (0);
	ft_bzero(array, size * count);
	return (array);
}
